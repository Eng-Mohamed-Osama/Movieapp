import 'dart:convert';

TvshowCast tvshowCastFromJson(String str) =>
    TvshowCast.fromJson(json.decode(str));

String tvshowCastToJson(TvshowCast data) => json.encode(data.toJson());

class TvshowCast {
  TvshowCast({
    this.cast,
    this.crew,
    this.id,
  });

  List<Cast> cast;
  List<Cast> crew;
  int id;

  factory TvshowCast.fromJson(Map<String, dynamic> json) => TvshowCast(
        cast: List<Cast>.from(json["cast"].map((x) => Cast.fromJson(x))),
        crew: List<Cast>.from(json["crew"].map((x) => Cast.fromJson(x))),
        id: json["id"],
      );

  Map<String, dynamic> toJson() => {
        "cast": List<dynamic>.from(cast.map((x) => x.toJson())),
        "crew": List<dynamic>.from(crew.map((x) => x.toJson())),
        "id": id,
      };
}

